    public static String centerMessage(String message, String spacer){
        int spacerPx = 0;
        for (var myChar : spacer.split("")) {
            spacerPx += regularLengths.get(myChar);
        }
        int currentPx = 0;
        for (var myChar : message.split("")) {
            int charVal = regularLengths.get(myChar);
            currentPx += charVal;
        }
        // center with spacer
        int spacerCount = 0;
        while (currentPx + (2*spacerPx) < CENTER_PX) {
            currentPx += (spacerPx*2);
            spacerCount++;
        }
        // center with space
        int spacePx = regularLengths.get("\u0020");
        int spaceCount = 0;
        while (currentPx + (2*spacePx) < CENTER_PX) {
            currentPx += (spacePx*2);
            spaceCount++;
        }
        return spacer.repeat(spacerCount) + " ".repeat(spaceCount);
    }

